package ru.netology.mysearchcompose

import androidx.lifecycle.ViewModel
import androidx.lifecycle.viewModelScope
import kotlinx.coroutines.flow.MutableStateFlow
import kotlinx.coroutines.flow.SharingStarted
import kotlinx.coroutines.flow.asStateFlow
import kotlinx.coroutines.flow.combine
import kotlinx.coroutines.flow.onEach
import kotlinx.coroutines.flow.stateIn
import kotlinx.coroutines.flow.update

class MainViewModel:ViewModel() {
    private val _searchText = MutableStateFlow("")
    val searchText = _searchText.asStateFlow()
    private  val _isSearching = MutableStateFlow(false)
    private  val _aircrafts = MutableStateFlow(allAircrafts)
    val isSearching = _isSearching.asStateFlow()
    val aircrafts = searchText
        .onEach { _isSearching.update { true } }
        .combine(_aircrafts){ text, aircrafts ->
            if(text.isBlank()){
                aircrafts
            } else {
                // delay(200L)
                aircrafts.filter {
                    it.doesMatchSearchQuery(text)
                }
            }
        }
        .onEach { _isSearching.update { false } }
        .stateIn(
            viewModelScope,
            SharingStarted.WhileSubscribed(5000),
            _aircrafts.value
        )
    fun onSearchTextChange(text:String){
        _searchText.value = text
    }

}
data class Aircraft(
    val acRusReg:String,
    val acForReg:String,
    val acType : String,
    val acSerialNum:String,
    val acEffNum : String
){
    fun doesMatchSearchQuery(query:String):Boolean{
        val matchingCombinations = listOf(
            "$acRusReg$acSerialNum",
            "$acRusReg $acSerialNum",
            "$acType $acSerialNum",
            "${acRusReg.first()}${acSerialNum.first()}",
        )

        return matchingCombinations.any {
            it.contains(query,ignoreCase = true)
        }
    }
}
